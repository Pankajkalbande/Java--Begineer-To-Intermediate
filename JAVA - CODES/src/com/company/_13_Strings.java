package com.company;

public class _13_Strings {
    public static void main(String[] args) {

        String fname = new String("Pankaj");
        System.out.println(fname);

        String lname = "Kalbande";
        System.out.println(lname);

//        String are immutable in java

        System.out.println("The name is: "+ fname +" "+ lname);

        int a = 10;
        int b = 20;
        System.out.printf("The value of a is %d and The value of b is %d", a,b);
        System.out.println("\n");
        System.out.format("The value of a is %d and The value of b is %d", a,b);



    }
}
